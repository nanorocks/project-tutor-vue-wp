class GroupRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Group::class);
    }


    public function create(array $params, bool $flush = false): Group
    {
        $group = new Group();
        $group->setName($params['name']);
        $group->setCreatedAt($params['created_at']);
        $group->setUpdatedAt($params['updated_at']);

        $group->setTutor($params['tutor']);

        $this->getEntityManager()->persist($group);

        // if ($flush) {
            $this->getEntityManager()->flush();
        // }

        return $group;
    }

    public function add(Group $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function remove(Group $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function delete(int $id, bool $flush = false): Group
    {
        $group = $this->find($id);

        if($group === null) throw new NotFoundHttpException('Record not exist.');

        $this->remove($group);

        return $group;
    }

